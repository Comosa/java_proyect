package calcuadora;



public class calculadora {
	int suma;
	int resta;
	int multiplicacion;
	int divicion;
	
	public int Sumar(int num1, int num2) {
		int suma= num1+num2;
		return suma;
	}
	public int reata(int num1, int num2) {
		int resta= num1-num2;
		return resta;
	}
	public int multiplicacion(int num1, int num2) {
		int multiplicacion= num1*num2;
		return multiplicacion;
	}
	public int  divicion(int num1, int num2) {
		int divicion= num1/num2;
		return divicion;
	}
}
